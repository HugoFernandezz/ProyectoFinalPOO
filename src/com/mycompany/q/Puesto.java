package com.mycompany.q;

/**
 * Enumeración que representa los diferentes puestos dentro de un equipo deportivo.
 * Los puestos disponibles son: entrenador, preparador, analista y scout.
 * 
 * @author HugoFDZ
 */
enum Puesto {
    /** Representa al entrenador del equipo */
    entrenador,
    
    /** Representa al preparador físico o técnico del equipo */
    preparador,
    
    /** Representa al analista del equipo, encargado de estudiar al rival o el rendimiento del equipo */
    analista,
    
    /** Representa al scout, que se dedica a la búsqueda de talento o jugadores */
    scout;
}

